#include "libft.h"

int		ft_count_num(int n)
{
	int len;

	len = 0;
	if (n == 0)
		return (1);
	if (n < 0)
	{
		len += 1;
		n *= -1;
	}
	while (n >= 1)
	{
		n = n / 10;
		len++;
	}
	return (len);
}
